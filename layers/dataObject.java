package layers;

public class dataObject {
	int integer;
	String string;
	
	public dataObject(int integer, String string) {
		super();
		this.integer = integer;
		this.string = string;
	}

	public int getInteger() {
		return integer;
	}

	public void setInteger(int integer) {
		this.integer = integer;
	}

	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}
	
	

}
